#include <stdio.h>

void	ft_bzero(void *s, size_t n)
{
	int				i;
	unsigned char	*buf;

	i = 0;
	buf = (unsigned char *)s;
	while (i < (int)n)
	{
		buf[i] = 0;
		i++;
	}
}
